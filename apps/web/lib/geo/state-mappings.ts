/**
 * Normalize geographic names according to project standards
 * Critical: Gulf of Mexico naming must be preserved
 */
export function normalizeGeographicName(name: string): string {
  // Enforce Gulf of Mexico naming standard
  if (name === 'Gulf of America') return 'Gulf of Mexico';
  return name;
}

